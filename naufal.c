#include "naufal.h"

float pertambahan(float nilai1, float nilai2){
	float temp;
	
	temp = nilai1 + nilai2;
	return temp;
}

float pengurangan(float nilai1, float nilai2){
	float temp;
	
	temp = nilai1 - nilai2;
	return temp;
}

float perkalian(float nilai1, float nilai2){
	float temp;
	
	temp = nilai1 * nilai2;
	return temp;
}

float pembagian(float nilai1, float nilai2){
	float temp;
	
	temp = nilai1 / nilai2;
	return temp;
}
