#include "naufal.h"

int pertambahan(int nilai1, int nilai2){
	int temp;
	
	temp = nilai1 + nilai2;
	return temp;
}

int pengurangan(int nilai1, int nilai2){
	int temp;
	
	temp = nilai1 - nilai2;
	return temp;
}

int perkalian(int nilai1, int nilai2){
	int temp;
	
	temp = nilai1 * nilai2;
	return temp;
}

float pembagian(float nilai1, float nilai2){
	float temp;
	
	temp = nilai1 / nilai2;
	return temp;
}
